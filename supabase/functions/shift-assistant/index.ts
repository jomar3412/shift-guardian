import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  "Access-Control-Allow-Origin": "*",
  "Access-Control-Allow-Headers":
    "authorization, x-client-info, apikey, content-type, x-supabase-client-platform, x-supabase-client-platform-version, x-supabase-client-runtime, x-supabase-client-runtime-version",
};

serve(async (req) => {
  if (req.method === "OPTIONS")
    return new Response(null, { headers: corsHeaders });

  try {
    const { messages, shiftContext } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    if (!LOVABLE_API_KEY) throw new Error("LOVABLE_API_KEY is not configured");

    const systemPrompt = `You are an AI shift-management assistant for a retail/service environment. You help managers make smart decisions about lunch break scheduling, coverage, and compliance.

You have access to the following real-time shift data:
${JSON.stringify(shiftContext, null, 2)}

KEY RULES:
- Employees MUST take lunch before the configured meal deadline to avoid a compliance violation.
- "policy.mealDeadlineHours" is the current deadline in hours for this company/state.
- "minutesToFifthHour" shows minutes remaining before that configured meal deadline. Lower = more urgent.
- When suggesting who should go to lunch, prioritize employees closest to the configured meal deadline.
- Always check coverage: if sending someone to lunch drops a role below its minimum coverage, warn the manager and suggest alternatives.
- "coverageProtection" roles have a "minCoverage" threshold that must be maintained.
- Employees can only be reassigned to sub-roles they are qualified for.

RESPONSE STYLE:
- Be concise and actionable. Use bullet points.
- If asked "who should go next?", give a ranked list with reasoning.
- If asked about coverage, show current vs minimum for protected roles.
- If asked "who can cover X?", list qualified and available employees.
- Always mention compliance risk level when relevant.`;

    const response = await fetch(
      "https://ai.gateway.lovable.dev/v1/chat/completions",
      {
        method: "POST",
        headers: {
          Authorization: `Bearer ${LOVABLE_API_KEY}`,
          "Content-Type": "application/json",
        },
        body: JSON.stringify({
          model: "google/gemini-3-flash-preview",
          messages: [{ role: "system", content: systemPrompt }, ...messages],
          stream: true,
        }),
      }
    );

    if (!response.ok) {
      if (response.status === 429) {
        return new Response(
          JSON.stringify({ error: "Rate limit exceeded. Please try again shortly." }),
          { status: 429, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      if (response.status === 402) {
        return new Response(
          JSON.stringify({ error: "AI usage limit reached. Please add credits in Settings → Workspace → Usage." }),
          { status: 402, headers: { ...corsHeaders, "Content-Type": "application/json" } }
        );
      }
      const t = await response.text();
      console.error("AI gateway error:", response.status, t);
      return new Response(
        JSON.stringify({ error: "AI service error" }),
        { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
      );
    }

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (e) {
    console.error("shift-assistant error:", e);
    return new Response(
      JSON.stringify({ error: e instanceof Error ? e.message : "Unknown error" }),
      { status: 500, headers: { ...corsHeaders, "Content-Type": "application/json" } }
    );
  }
});
